def narigoma(koma):
    if koma == '歩':
        return "と"
    if koma == '香':
        return "杏"
    if koma == '桂':
        return "圭"
    if koma == '銀':
        return "全"
    if koma == '飛':
        return "龍"
    if koma == '角':
        return "馬"


